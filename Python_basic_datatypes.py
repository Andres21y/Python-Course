#---BASIC DATA TYPES

# Integer without decimals number
intDatatypes = 12

print(type(intDatatypes),intDatatypes)

# Float, only decimals number
floatDatatypes = 3.14

print(type(floatDatatypes),floatDatatypes)

# Boolean, True or False
boolDatatypes = False

print(type(boolDatatypes),boolDatatypes)

# String 
srtDatatypes = "Hola Mundo Cruel"

print(type(srtDatatypes),srtDatatypes)

