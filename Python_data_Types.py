# These can be (Numeric,Sequence, Dictionary, Boolean and Set)

##----NUMERIC----

# Integer without decimals number
intDatatypes = 12
print(type(intDatatypes),intDatatypes)

# Float, only decimals number
floatDatatypes = 3.14
print(type(floatDatatypes),floatDatatypes)

##---Sequence----

# String 
srtDatatypes = "Hola Mundo Cruel"
print(type(srtDatatypes),srtDatatypes)

# List 
listDatatypes = [1,2,3,4,"wey"]
print(type(listDatatypes),listDatatypes)

# Tuples
tupDatatypes = (1,2,"wey")
print(type(tupDatatypes),tupDatatypes)

##---Dictionary----
dicDatatypes = {"name":"peter","Age":30}
print(type(dicDatatypes),dicDatatypes)

##---Boolean----
boolDatatypes = False
print(type(boolDatatypes),boolDatatypes)

##---Set----
setDatatypes = {"apple","orange","pear"}
print(type(setDatatypes),setDatatypes)